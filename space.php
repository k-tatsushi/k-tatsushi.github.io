<?php
function divisibleNumbers($a, $b) {
    $numbers = [];
    for ($i = $a; $i <= $b; $i++) {
        if ($i % 4 == 0) {
            $numbers[] = $i;
        }
    }
    foreach ($numbers as $number) {
        echo $number . PHP_EOL;
    }
}
